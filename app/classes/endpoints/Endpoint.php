<?php

namespace API;

/**
 * Class Endpoint
 * @package API
 */
abstract class Endpoint
{
    protected int $employee;
    protected bool $manager;
    protected object $db;

    public function __construct(int $employee, bool $manager)
    {
        $this->employee = $employee;
        $this->manager = $manager;
        $this->db = new \Database;
    }
}